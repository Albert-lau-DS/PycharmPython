num = int(input())
s = '' # s是字符串，所以在第37行可以让他直接与字符串相加，也就是拼接起来
if num == 0:
    s = '0'
while num != 0: #这里的num是输入，也是求商后的结果
    s = s + str(num % 2)  # 将num对2取余的结果转字符串拼接到s
    num = num // 2
print(s[::-1])# s[::-1]为逆向取数

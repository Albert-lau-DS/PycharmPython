"""
描述
编写程序，计算2个正整数的和、差、积、商并输出。题目保证输入和输出全部在整型范围内且除数不为0。
输入格式
在第一行中接收一个正整数A
在第二行中接收一个正整数B
输出格式
在4行中按照格式“A 运算符 B = 结果”顺序输出和、差、积、商。
"""
A = int(input())
B = int(input())
print("{} + {} = {}".format(A,B,A+B))
print("{} - {} = {}".format(A,B,A-B))
print("{} * {} = {}".format(A,B,A*B))
print("{} / {} = {}".format(A,B,A/B))